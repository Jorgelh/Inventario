    private void listarproductos(){
        ArrayList<Classp> result = consultas.ListarProductos(descripcion.getText());
        productos(result);
    }
    
    private void productos(ArrayList<Classp> list){
        
         Object[][] datos = new Object[list.size()][4];
              int i = 0;
              for(Classp t : list)
              {
                  datos[i][0] = t.getCodigo();
                  datos[i][1] = t.getDescripcion();
                  i++;
              }    
             TablaProductos.setModel(new javax.swing.table.DefaultTableModel(
                datos,
                new String[]{
                "CODIGO","DESCRIPCION"
             })
             {  
                 @Override
             public boolean isCellEditable(int row, int column){
                 return false;
             }
             });
              TableColumn columna1 = TablaProductos.getColumn("CODIGO");
            columna1.setPreferredWidth(0);
            TableColumn columna2 = TablaProductos.getColumn("DESCRIPCION");
            columna2.setPreferredWidth(600);
    }
    private void listarpn(){
        ArrayList<Classp> result = consultas.Listarpn(codigo);
        listar(result);
    }
    private void listar(ArrayList<Classp> list){
        
         Object[][] datos = new Object[list.size()][4];
              int i = 0;
              for(Classp t : list)
              {
                  datos[i][0] = t.getDescripcion();
                  i++;
              }    
             PNUTIL.setModel(new javax.swing.table.DefaultTableModel(
                datos,
                new String[]{
                "P/N QUE UTILIZAN ESTE MATERIAL"
             })
             {  
                 @Override
             public boolean isCellEditable(int row, int column){
                 return false;
             }
             });
    }
