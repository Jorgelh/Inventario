    private void actualizarTablaconsulta() {

        ArrayList<ClassKardex> result = BDKardex.ListarProductoIngresado(Integer.parseInt(codigo.getText()));
        recagarTabla(result);
    }

    private void recagarTabla(ArrayList<ClassKardex> list) {

        Object[][] dato = new Object[list.size()][11];
        int f = 0;
        for (ClassKardex a : list) {
            dato[f][0] = a.getTipo();
            dato[f][1] = a.getFecha();
            dato[f][2] = a.getCantidadin();
            dato[f][3] = a.getPrecioin();
            dato[f][4] = a.getTotalin();
            dato[f][5] = a.getCantidadout();
            dato[f][6] = a.getPrecioout();
            dato[f][7] = a.getTotalout();
            dato[f][8] = a.getCantidadsaldo();
            dato[f][9] = a.getPreciosaldo();
            dato[f][10] = a.getTotalsaldo();
            f++;
        }
        Karde.setModel(new javax.swing.table.DefaultTableModel(
                dato,
                new String[]{
                    "TIPO","FECHA","CANTIDAD INGRESO","PRECIO INGRESO","TOTAL INGRESO","CANTIDAD DESCARGA","PRECIO DESCARGA","TOTAL DESCARGA","CANTIDAD SALDO","PRECIO SALDO","TOTAL SALDO"
                }) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        });
    }
    
    
