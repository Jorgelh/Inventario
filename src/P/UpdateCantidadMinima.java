    private void actualizarTablaNombre() {

        ArrayList<Producto> result = BDConsultas.ListarCantidadMinima(nombre.getText());
        recagarTabla(result);
    }

    private void recagarTabla(ArrayList<Producto> list) {

        Object[][] dato = new Object[list.size()][7];
        int f = 0;
        for (Producto a : list) {
            dato[f][0] = a.getCodigo();
            dato[f][1] = a.getDescripcion();
            dato[f][2] = a.getCantidadminima();
            dato[f][3] = a.getPresentacion();
            dato[f][4] = a.getUmedida();
            dato[f][5] = a.getUbicacion();
            dato[f][6] = a.getNota();
            f++;
        }
        tablanom.setModel(new javax.swing.table.DefaultTableModel(
                dato,
                new String[]{
                    "CODIGO", "DESCRIPCION", "CANTIDAD MINIMA", "PRESENTACION","UNIDAD DE MEDIDA","UBICACION","NOTA"
                }) {
                    @Override
                    public boolean isCellEditable(int row, int column) {
                        return false;
                    }
                });
    }
    
