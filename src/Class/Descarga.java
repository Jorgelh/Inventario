public class Descarga {
    
    
   private int in_descarga;
   private int id_ingreso;
   private int codigo;
   private String fecha;
   private int Cantidad;
   private int EntregadoA;
   private double nota;

    public int getIn_descarga() {
        return in_descarga;
    }

    public void setIn_descarga(int in_descarga) {
        this.in_descarga = in_descarga;
    }

    public int getId_ingreso() {
        return id_ingreso;
    }

    public void setId_ingreso(int id_ingreso) {
        this.id_ingreso = id_ingreso;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public int getCantidad() {
        return Cantidad;
    }

    public void setCantidad(int Cantidad) {
        this.Cantidad = Cantidad;
    }

    public int getEntregadoA() {
        return EntregadoA;
    }

    public void setEntregadoA(int EntregadoA) {
        this.EntregadoA = EntregadoA;
    }

    public double getNota() {
        return nota;
    }

    public void setNota(double nota) {
        this.nota = nota;
    }
   
   
   
    
            
    
    
}
