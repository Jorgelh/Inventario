public class consultanp {
    
    private int codigo;
    private String no_trabajo;
    private String lote;
    private String descripcion;
    private String fechades;
    private int    cantidad;
    private String entregadoa;

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getNo_trabajo() {
        return no_trabajo;
    }

    public void setNo_trabajo(String no_trabajo) {
        this.no_trabajo = no_trabajo;
    }

    public String getLote() {
        return lote;
    }

    public void setLote(String lote) {
        this.lote = lote;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getFechades() {
        return fechades;
    }

    public void setFechades(String fechades) {
        this.fechades = fechades;
    }

    public int getCantidad() {
        return cantidad;
    }

    public void setCantidad(int cantidad) {
        this.cantidad = cantidad;
    }

    public String getEntregadoa() {
        return entregadoa;
    }

    public void setEntregadoa(String entregadoa) {
        this.entregadoa = entregadoa;
    }
    
    
    
    
    
}
